namespace UETK7.UnrealEngine
{
    /// <summary>
    /// An enumeration type for unreal engine 4 versions used throughout Tekken 7 development.
    /// </summary>
    public enum UE4Version
    {
        /// <summary>
        /// The unreal engine version used for Tekken 7.0 (2015).
        /// </summary>
        UE4__4_4_0_0_r2256484,
        /// <summary>
        /// The unreal engine version used for Tekken 7 Fated Retribution (2016).
        /// </summary>
        UE4__4_9_2_0_deport_release,
        /// <summary>
        /// The unreal engine version used for Tekken 7's console release.
        /// </summary>
        UE4__4_14,
        /// <summary>
        /// Undefined unreal engine 4 version.
        /// </summary>
        Undefined
    }
}
